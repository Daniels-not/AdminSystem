    private void tblUsersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsersMouseClicked
        // TODO add your handling code here:
        if(evt.getClickCount() == 1){ // al darle solo un click 
            JTable resividor = (JTable) evt.getSource(); // buscaremos la columna
            
            // le pasaremos el dato de la columna los txt
            txtNombre.setText(resividor.getModel().getValueAt(resividor.getSelectedRow(), 0).toString());
            txtApellido.setText(resividor.getModel().getValueAt(resividor.getSelectedRow(), 1).toString());
            txtTelefono.setText(resividor.getModel().getValueAt(resividor.getSelectedRow(), 2).toString());
            txtCorreo.setText(resividor.getModel().getValueAt(resividor.getSelectedRow(), 3).toString());
            txtUsuario.setText(resividor.getModel().getValueAt(resividor.getSelectedRow(), 4).toString());
        }
        
        btnAñadir.setEnabled(false);
        btnEliminar.setEnabled(true);
        btnEditar.setEnabled(true);
    }//GEN-LAST:event_tblUsersMouseClicked

