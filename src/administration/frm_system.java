    // mostrar los datos de la dba en la tabla
    public void desplegarDatos(){
        sqlMethods metodo = new sqlMethods();
        
        while(modelo.getRowCount() > 0){
            modelo.removeRow(0);
        }
    
        try {
               ResultSet resultado = metodo.consultarDatos("SELECT * FROM adminSystem");
                                
               while(resultado.next()){

                    String[] usuario = {resultado.getString("nombre"), resultado.getString("apellido"), resultado.getString("telefono"), resultado.getString("correo"), resultado.getString("nombre_usuario")};
                    modelo.addRow(usuario);
               }
        } catch (Exception e) {
            System.out.println("Opps algo ocurrio procesando los datos " + e);
        }
    }
    
    public administradorBL consultarDatos(){
        
        administradorBL adminSend = new administradorBL();
        
        adminSend.setNombre(txtNombre.getText());
        adminSend.setApellido(txtApellido.getText());
        adminSend.setTelefono(txtTelefono.getText());
        adminSend.setCorreo(txtCorreo.getText());
        adminSend.setUsuario(txtUsuario.getText());
        
        return adminSend;
    }
    
